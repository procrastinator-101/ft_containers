#ifndef VECTORITERATOR_HPP
# define VECTORITERATOR_HPP

#include <iterator>

# include "../iterator/iterator_traits.hpp"


namespace ft
{
	template <class Pointer>
	class vectorIterator
	{
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// type definitions
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		public:
			typedef std::random_access_iterator_tag iterator_category;
			typedef typename iterator_traits<Pointer>::value_type value_type;
			typedef typename iterator_traits<Pointer>::difference_type difference_type;
			typedef typename iterator_traits<Pointer>::pointer pointer;
			typedef typename iterator_traits<Pointer>::reference reference;

			typedef const pointer const_pointer;
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// type definitions End
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		


		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Constructors, assignment operators, and destructors
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		public:
			vectorIterator() : _ptr(0)
			{
			}

			vectorIterator(pointer ptr) : _ptr(ptr)
			{
			}

			vectorIterator(const vectorIterator& src) : _ptr(src._ptr)
			{
			}

			vectorIterator	&operator=(const vectorIterator& rop)
			{
				if (this == &rop)
					return *this;
				_ptr = rop._ptr;
				return *this;
			}

			~vectorIterator()
			{
			}
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Constructors, assignment operators, and destructors End
		/////////////////////////////////////////////////////////////////////////////////////////////////////


		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Comparison operators
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		public:
			bool	operator==(const vectorIterator& rop)
			{
				return _ptr == rop._ptr;
			}

			bool	operator!=(const vectorIterator& rop)
			{
				return _ptr != rop._ptr;
			}

			bool	operator<(const vectorIterator& rop)
			{
				return _ptr < rop._ptr;
			}

			bool	operator>(const vectorIterator& rop)
			{
				return _ptr > rop._ptr;
			}

			bool	operator<=(const vectorIterator& rop)
			{
				return _ptr <= rop._ptr;
			}

			bool	operator>=(const vectorIterator& rop)
			{
				return _ptr >= rop._ptr;
			}
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Comparison operators End
		/////////////////////////////////////////////////////////////////////////////////////////////////////

		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Arithmetic operators
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		public:
			vectorIterator	operator+(difference_type d)
			{
				return vectorIterator(_ptr + d);
			}

			vectorIterator	operator-(difference_type d)
			{
				return vectorIterator(_ptr - d);
			}

			vectorIterator	operator-(const vectorIterator& rop)
			{
				return vectorIterator(_ptr - rop._ptr);
			}

			vectorIterator	&operator+=(difference_type d)
			{
				_ptr += d;
				return *this;
			}

			vectorIterator	&operator-=(difference_type d)
			{
				_ptr -= d;
				return *this;
			}

			vectorIterator	&operator++()
			{
				_ptr++;
				return *this;
			}

			vectorIterator	operator++(int n)
			{
				vectorIterator	ret(*this);

				(void)n;
				_ptr++;
				return ret;
			}

			vectorIterator	&operator--()
			{
				_ptr--;
				return *this;
			}

			vectorIterator	operator--(int n)
			{
				vectorIterator	ret(*this);

				(void)n;
				_ptr--;
				return ret;
			}
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Arithmetic operators End
		/////////////////////////////////////////////////////////////////////////////////////////////////////


		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Derefrence operators
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		public:
			pointer	operator->()
			{
				return _ptr;
			}

			value_type	&operator*()
			{
				return *_ptr;
			}

			value_type	&operator[](difference_type n)
			{
				return _ptr[n];
			}
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Derefrence operators End
		/////////////////////////////////////////////////////////////////////////////////////////////////////

		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Data members
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		private:
			pointer	_ptr;
		/////////////////////////////////////////////////////////////////////////////////////////////////////
		/// Data members End
		/////////////////////////////////////////////////////////////////////////////////////////////////////
	};
}

#endif